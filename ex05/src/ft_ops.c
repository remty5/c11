int	ft_sum(int a, int b)
{
	return (a + b);
}

int	ft_diff(int a, int b)
{
	return (a - b);
}

int	ft_div(int a, int b)
{
	return (a / b);
}

int	ft_prod(int a, int b)
{
	return (a * b);
}

int	ft_mod(int a, int b)
{
	return (a % b);
}
